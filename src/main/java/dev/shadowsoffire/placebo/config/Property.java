    /**
     * Sets the maximum length of this list/array Property. Only important if isList() == true. If
     * the current values array or default values array is longer than the new maximum it will be
     * resized. If calling both this method and setIsListLengthFixed(true), this method should be
     * called afterwards (but is not required).
     */
    public Property setMaxListLength(int max) {
        this.maxListLength = max;
        if (this.maxListLength != -1) {
            if (this.values != null && this.values.length != this.maxListLength)
                if (this.isListLengthFixed || this.values.length > this.maxListLength)
                    this.values = Arrays.copyOf(this.values, this.maxListLength);

            if (this.defaultValues != null && this.defaultValues.length != this.maxListLength)
                if (this.isListLengthFixed || this.defaultValues.length > this.maxListLength)
                    this.defaultValues = Arrays.copyOf(this.defaultValues, this.maxListLength);
        }
        return this;
    }

