    /**
     * Returns whether or not this Property is defaulted.
     *
     * @return true if the current value(s) is(are) deeply equal to the default value(s)
     */
    public boolean isDefault() {
        if (this.isBooleanList()) {
            if (this.values.length == this.defaultValues.length) {
                for (int i = 0; i < this.values.length; i++)
                    if (Boolean.parseBoolean(this.values[i])
                            != Boolean.parseBoolean(this.defaultValues[i])) return false;

                return true;
            } else return false;
        }

        if (this.isIntList()) {
            if (this.values.length == this.defaultValues.length) {
                for (int i = 0; i < this.values.length; i++)
                    if (Integer.parseInt(this.values[i]) != Integer.parseInt(this.defaultValues[i]))
                        return false;

                return true;
            } else return false;
        }

        if (this.isDoubleList()) {
            if (this.values.length == this.defaultValues.length) {
                for (int i = 0; i < this.values.length; i++)
                    if (Double.parseDouble(this.values[i])
                            != Double.parseDouble(this.defaultValues[i])) return false;

                return true;
            } else return false;
        }

        if (this.isList()) {
            if (this.values.length == this.defaultValues.length) {
                for (int i = 0; i < this.values.length; i++)
                    if (!this.values[i].equals(this.defaultValues[i])) return false;

                return true;
            } else return false;
        }

        if (this.type == Type.BOOLEAN && this.isBooleanValue())
            return Boolean.parseBoolean(this.value) == Boolean.parseBoolean(this.defaultValue);

        if (this.type == Type.INTEGER && this.isIntValue())
            return Integer.parseInt(this.value) == Integer.parseInt(this.defaultValue);

        if (this.type == Type.DOUBLE && this.isDoubleValue())
            return Double.parseDouble(this.value) == Double.parseDouble(this.defaultValue);

        return this.value.equals(this.defaultValue);
    }

    /** Sets the current value(s) of this Property to the default value(s). */
    public Property setToDefault() {
        this.value = this.defaultValue;
        this.values = Arrays.copyOf(this.defaultValues, this.defaultValues.length);
        return this;
    }

