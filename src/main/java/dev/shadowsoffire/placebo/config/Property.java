    public enum Type {
        STRING,
        INTEGER,
        BOOLEAN,
        DOUBLE,
        COLOR,
        MOD_ID;

        public static Type tryParse(char id) {
            for (int x = 0; x < values().length; x++) {
                if (values()[x].getID() == id) {
                    return values()[x];
                }
            }

            return STRING;
        }

        public char getID() {
            return this.name().charAt(0);
        }
    }

